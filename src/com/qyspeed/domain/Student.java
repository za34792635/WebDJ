package com.qyspeed.domain;

public class Student {

	private String studentNumber;//学生编号
	private String studentName;//学生名
	private String studentPassword;//学生密码
	private String studentSex;//学生性别
	private ClassInfo studentClassNumber;//学生班级
	private String studentBirthday;//出生日期
	private String studentState;//政治面貌
	private String studentPhoto;//学生照片
	private String studentTelephone;//学生电话
	private String studentEmail;//学生邮箱
	private String studentQQ;//学生QQ
	private String studentAddress;//学生地址
	private String studentMemo;//学生备注
	
	public String getStudentNumber() {
		return studentNumber;
	}
	public void setStudentNumber(String studentNumber) {
		this.studentNumber = studentNumber;
	}
	public String getStudentName() {
		return studentName;
	}
	public void setStudentName(String studentName) {
		this.studentName = studentName;
	}
	public String getStudentPassword() {
		return studentPassword;
	}
	public void setStudentPassword(String studentPassword) {
		this.studentPassword = studentPassword;
	}
	public String getStudentSex() {
		return studentSex;
	}
	public void setStudentSex(String studentSex) {
		this.studentSex = studentSex;
	}
	public ClassInfo getStudentClassNumber() {
		return studentClassNumber;
	}
	public void setStudentClassNumber(ClassInfo studentClassNumber) {
		this.studentClassNumber = studentClassNumber;
	}
	public String getStudentBirthday() {
		return studentBirthday;
	}
	public void setStudentBirthday(String studentBirthday) {
		this.studentBirthday = studentBirthday;
	}
	public String getStudentState() {
		return studentState;
	}
	public void setStudentState(String studentState) {
		this.studentState = studentState;
	}
	public String getStudentPhoto() {
		return studentPhoto;
	}
	public void setStudentPhoto(String studentPhoto) {
		this.studentPhoto = studentPhoto;
	}
	public String getStudentTelephone() {
		return studentTelephone;
	}
	public void setStudentTelephone(String studentTelephone) {
		this.studentTelephone = studentTelephone;
	}
	public String getStudentEmail() {
		return studentEmail;
	}
	public void setStudentEmail(String studentEmail) {
		this.studentEmail = studentEmail;
	}
	public String getStudentQQ() {
		return studentQQ;
	}
	public void setStudentQQ(String studentQQ) {
		this.studentQQ = studentQQ;
	}
	public String getStudentAddress() {
		return studentAddress;
	}
	public void setStudentAddress(String studentAddress) {
		this.studentAddress = studentAddress;
	}
	public String getStudentMemo() {
		return studentMemo;
	}
	public void setStudentMemo(String studentMemo) {
		this.studentMemo = studentMemo;
	}
	
}
